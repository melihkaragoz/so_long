#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*new;
	int		len;
	int		i;

	len = (int)(ft_strlen(s1));
	new = malloc(sizeof(char) * (len + 1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = 0;
	return (new);
}
